# What is a set in Python?
    # A set is a collection of unique elements.
    # It does not allow duplicate values.
    # Sets are unordered, so elements have no fixed position(order is not guaranteed).
    # Sets are mutable (we can add or remove elements).

s = {1, 2, 3, 3}
print(s)
print(type(s))  # <class 'set'> 
# Output will be {1, 2, 3} because sets do not allow duplicates.
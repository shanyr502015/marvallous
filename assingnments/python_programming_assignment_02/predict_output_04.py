print("10" + "20") # 1020
print(10 + 20) # 30
# Explanation
    # In the first line, "10" and "20" are strings (text inside quotes).
    # The + operator concatenates (joins) two strings.          
    # So, "10" + "20" results in "1020".
    # In the second line, 10 and 20 are integers (numbers without quotes).
    # The + operator adds two integers.         
    # So, 10 + 20 results in 30.

range(1, 10) # 1, 2, 3, 4, 5, 6, 7, 8, 9
    # Start = 1
    # Stop = 10 (not included)
    # Step = 1 (default)
range(1, 10, 2) # 1, 3, 5, 7, 9
    # Start = 1
    # Stop = 10 (not included)
    # Step = 2

# range(start, stop, step)
    # start → number to begin from
    # stop → number to stop before (not included)
    # step → gap between numbers
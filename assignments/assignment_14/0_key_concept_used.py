# Lambda Syntax
    # lambda parameters: expression
    # Lambda functions are anonymous (unnamed) functions
    # They can have multiple parameters but only one expression
    # The expression is automatically returned

# Ternary Operator:
    # value_if_true if condition else value_if_false
    # Used for conditional expressions in lambda functions

# Lambda Functions are Useful for:
    # Short, simple operations
    # One-line functions
    # Functional programming techniques

# Advantages of Lambda Functions:
    # Concise and readable for simple operations
    # Can be used inline without defining a separate function
    # Useful with higher-order functions like map(), filter(), sorted()
    # Saves lines of code for trivial operations
    # Enhances code readability when used appropriately

# When to Use Lambda vs Regular Functions:
    # Use Lambda when:
        # The function is simple (one expression)
        # It's used only once or in a specific context
        # Used with map(), filter(), sorted(), etc.
    # Use Regular Functions when:
        # The logic is complex (multiple statements)
        # The function will be reused multiple times
        # You need better documentation and debugging
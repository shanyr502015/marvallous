# Differentiate between calling a function and defining a function with a simple explanation.

# Function Definition vs Function Call
    # Defining a function means creating the function and writing its logic.
    # Calling a function means executing the function to perform its task.
    # A function does nothing until it is called.

# Defining a Function
    # Writing the function using the `def` keyword and specifying what it should do.

# Important Points:
    # Uses `def` keyword.
    # Function logic is written here.
    # Executed only when called.
    # Written once, used many times.

# Function definition
def add(a, b):
    return a + b
# This is just a definition; nothing happens yet.

# Calling a Function: Using the function name to execute the function logic.

# Important Points:
    # Function name followed by parentheses.
    # Can pass arguments while calling.
    # Executes the function body.

# Function call
result = add(10, 20)
print(result) # 30

# Simple Difference Table:
    # --------------------------------------------------
    # | Defining a Function  | Calling a Function       |
    # | -------------------- | ------------------------ |
    # | Creates the function | Executes the function    |
    # | Uses `def` keyword   | Uses function name       |
    # | No output by itself  | Produces output          |
    # | Written once         | Can be called many times |
    # ---------------------------------------------------